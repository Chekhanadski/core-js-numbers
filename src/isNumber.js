/**
 * Returns a boolean value indicating whether the parameter is a number or not.
 *
 * @param {number} number
 * @return {boolean}
 *
 * @example:
 * Infinity => false
 * NaN      => false
 * 0        => true
 * 'a' / 1  => false
 * 'a'      => false
 * 5        => true
 * '5'      => false
 */
function isNumber(number) {
  return (
    typeof number === 'number' &&
    !Number.isNaN(number) &&
    Number.isFinite(number)
  );
}

isNumber();
